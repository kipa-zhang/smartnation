package org.st.smartnation.bean;

/**
 * 坐标类
 * @author shizhou
 *
 */
public class Coordinate {

	private String lon;
	private String lat;
	
	public String getLon() {
		return lon;
	}
	public void setLon(String lon) {
		this.lon = lon;
	}
	public String getLat() {
		return lat;
	}
	public void setLat(String lat) {
		this.lat = lat;
	}
	
}
